$("#readmore1").click(function() {
if ($("#more1").hasClass("hidden")) {
$("#more1").removeClass("hidden");
$("#readless1").removeClass("hidden");
$("#readmore1").addClass("hidden");
$("#less1").addClass("hidden"); } else {
$("#more1").addClass("hidden"); }
});

